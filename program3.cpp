#include <iostream>
using namespace std;

int main() {
    // Chương trình này minh họa một vòng lặp while đơn giản.

    int number = 0;

    while (number < 5) {
        cout << "Xin chào\n"; // Hiển thị "Xin chào"
        number++;
    }

    cout << "Đó là tất cả!\n"; // Hiển thị "Đó là tất cả!"
    return 0;
}
